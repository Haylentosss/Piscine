#include<unistd.h>

int	ft_str_is_alpha(char	*str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		if ((str[b] < 'A' ) || (str[b] > 'Z' && str[b] < 'a') || (str[b] > 'z'))
			return (0);
		b++;
	}
	return (1);
}
